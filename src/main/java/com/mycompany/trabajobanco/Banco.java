    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        cliente = listaClientes.get(jComboBox3.getSelectedIndex());
        cuenta = cliente.getMisCuentas().get(jComboBox4.getSelectedIndex());
        Movimiento m= new Movimiento();
        m.setFechaMovimiento(new SimpleDateFormat("DD/MM/YYYY").format(new Date()));
        m.setTipoMoviento(jComboBox5.getSelectedItem().toString());
        double monto = Double.parseDouble(jTextField5.getText().toString());
        monto = m.getTipoMoviento().endsWith("DEPOSITO")?monto:(monto*-1);
        m.setMonto(monto);
        cuenta.addMovimiento(m);
        verMovimientos();
    }//GEN-LAST:event_jButton4ActionPerformed
    public String aMoneda(double cantidad){
        
        cantidad=Math.round(cantidad*100.0)/100.0;
        DecimalFormat formato = new DecimalFormat("$ #,###,###.##");
        return formato.format(cantidad);
    }
    public void verDatos (){
        cliente = listaClientes.get(jComboBox3.getSelectedIndex());
        jLabel23.setText(cliente.getNombre());
        jLabel24.setText(cliente.getTelefono());
        jLabel25.setText(cliente.getDireccion());
        
        if (cliente.getMisCuentas().size()>0){
            cuenta=cliente.getMisCuentas().get(jComboBox4.getSelectedIndex());
            jLabel26.setText(cuenta.getTipoCuenta());
            jLabel27.setText(aMoneda(cuenta.getMontoInicial()));
        
        }else{
            jLabel26.setText("NO HAY CUENTA");
            jLabel27.setText("NO HAY CUENTA");
            
        
        }
        
    
    }
    public void verMovimientos(){
        
        cliente = listaClientes.get(jComboBox3.getSelectedIndex());
        cuenta = cliente.getMisCuentas().get(jComboBox4.getSelectedIndex());
        double saldo =0;
        while(modelMovs.getRowCount()>0){
            modelMovs.removeRow(0);
        
        }
        for (Movimiento m: cuenta.getMisMovimientos()) {
            
            Object mov[] = new Object[4];
            mov[0]=cuenta.getTipoCuenta();
            mov[1]=m.getFechaMovimiento();
            mov[2]=m.getTipoMoviento();
            mov[3]=aMoneda(m.getMonto());
            saldo+=m.getMonto();
            modelMovs.addRow(mov);
            
            
            
            
        }
        jTable1.setModel(modelMovs);
        jLabel22.setText(aMoneda(saldo));
     
    }
    public void refrescarComboCuentas(){
        cliente = listaClientes.get(jComboBox3.getSelectedIndex());
        int i=0;
        ArrayList<String> cuentas= new ArrayList<String>();
        for (Cuenta c: cliente.getMisCuentas()){
            cuentas.add(c.getTipoCuenta());
            
        }
        jComboBox4.setModel(new DefaultComboBoxModel(cuentas.toArray()));
                
    
    }
    public void llenarCombosTipoCuenta(){
      Object tipos []= new Object [listaTipoCuenta.size()];
        int i=0;
        for (String tipo: listaTipoCuenta){
            tipos [i] = tipo;
            i++;  
        }
        jComboBox2.setModel(new DefaultComboBoxModel(tipos));
        
    }
    public void llenarComboxCliente(){
        
        Object Clientes []= new Object [listaClientes.size()];
        int i=0;
        for (Cliente c: listaClientes){
            Clientes [i] = c.getNombre();
            i++;
        
        }
        
        jComboBox1.setModel(new DefaultComboBoxModel(Clientes));
        jComboBox3.setModel(new DefaultComboBoxModel(Clientes));
    
    }
